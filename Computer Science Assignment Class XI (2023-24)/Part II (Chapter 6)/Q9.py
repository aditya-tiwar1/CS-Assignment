# Read a number and print its square, cube, and power of 4

number = float(input("Enter a number: "))
square = number ** 2
cube = number ** 3
power_of_4 = number ** 4
print("Square:", square)
print("Cube:", cube)
print("Power of 4:", power_of_4)

