# Program to calculate A to the power B
import random

a = random.randint(0, 5)
b = random.randint(0, 5)

result = a ** b

print(f"Random number between 0 and 5 (A): {a}")
print(f"Random number between 0 and 5 (B): {b}")
print(f"A to the power B = {result}")

# Output: A to the power B = [calculated value]